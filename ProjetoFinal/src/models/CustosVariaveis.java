package models;

public class CustosVariaveis {
 
   private int valor;
   private String denominacao;

   
    public int getValor() {
        return valor;
    }

    
    public void setValor(int valor) {
        this.valor = valor;
    }

    
    public String getDenominacao() {
        return denominacao;
    }

    
    public void setDenominacao(String denominacao) {
        this.denominacao = denominacao;
    }
   
}
